    public void ListarUsuarios() {
        String sql = "Select usuario, senha, salliq, parcelas_ext from usuario where usuario = '" + this.objModeloJFrame1.getUserlog() + "'";
        try {
            pst = conecta.prepareStatement(sql);
            rs = pst.executeQuery();
            pst.getResultSet();
            tblresult.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (SQLException error) {

            JOptionPane.showMessageDialog(null, "erro");

        }
    }

    public void mostraItens() {
        int seleciona = tblresult.getSelectedRow();
        txtUsuario.setText(tblresult.getModel().getValueAt(seleciona, 0).toString());
        txtSenha.setText(tblresult.getModel().getValueAt(seleciona, 1).toString());
        txtSal.setText(tblresult.getModel().getValueAt(seleciona, 2).toString());
        txtPcl.setText(tblresult.getModel().getValueAt(seleciona, 3).toString());

    }

    public void editarUsuarios() {
        String sql = "Update usuario set usuario = ?, senha = ?, salliq = ?, parcelas_ext = ? where usuario = '" + this.objModeloJFrame1.getUserlog() + "'";
        try {
            pst = conecta.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            pst.setString(2, txtSenha.getText());
            pst.setDouble(3, Double.parseDouble(txtSal.getText()));
            pst.setDouble(4, Double.parseDouble(txtPcl.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Editado com sucesso");
            ListarUsuarios();

        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, "Erro ao editar");
        }
    }

    public void excluirUsuario() {
        String sql = "Delete from usuario where usuario = '" + this.objModeloJFrame1.getUserlog() + "'";
        try {
            pst = conecta.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Deletado com sucesso");
            dispose();

        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir");
        }
    }

